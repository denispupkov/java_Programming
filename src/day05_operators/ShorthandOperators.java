package day05_operators;

public class ShorthandOperators {

    public static void main(String[] args) {

        int a = 20;

        System.out.println(a);
        System.out.println(a);
        a = 40;
        System.out.println(a);
        a = 90;
        System.out.println(a);
        System.out.println("___________________");

        double balance = 100;

        balance+=1000;

        System.out.println(balance);

        balance+=500;

        System.out.println(balance);



    }
}
